def translate_use_case_name(name: str) -> str:
    """Translate a full use case name (phrase) from Spanish to English.

    Translates every word: verbs, nouns, prepositions, articles.
    """
    if not name:
        return name

    # Build case-insensitive lookup for TRANSLATION_TABLE
    lower_tt = {k.lower(): v for k, v in TRANSLATION_TABLE.items()}

    words = name.split()
    translated = []

    for i, word in enumerate(words):
        wl = word.lower()

        # Try verb translation (especially for first word, but also mid-sentence)
        if i == 0 or wl in UC_VERB_MAP or wl in METHOD_TRANSLATIONS:
            if wl in UC_VERB_MAP:
                en = UC_VERB_MAP[wl]
                translated.append(en.capitalize() if i == 0 else en)
                continue
            if wl in METHOD_TRANSLATIONS:
                en = METHOD_TRANSLATIONS[wl]
                translated.append(en.capitalize() if i == 0 else en)
                continue

        # Try small words (prepositions, articles)
        if wl in SMALL_WORDS_MAP:
            en = SMALL_WORDS_MAP[wl]
            if en:  # skip empty translations (like 'se')
                translated.append(en)
            continue

        # Try UC_NOUN_MAP
        if wl in UC_NOUN_MAP:
            en = UC_NOUN_MAP[wl]
            # Preserve capitalization of first word
            if i == 0:
                translated.append(en.capitalize())
            else:
                translated.append(en)
            continue

        # Try TRANSLATION_TABLE (case-insensitive)
        if wl in lower_tt:
            en = lower_tt[wl]
            if i == 0:
                translated.append(en.capitalize() if en[0].islower() else en)
            else:
                translated.append(en)
            continue

        # Keep original (proper noun, acronym, or already English)
        translated.append(word)

    return ' '.join(translated)


def translate_class_diagram(cd: dict) -> dict:
    """Translate all labels in a class diagram to English."""
    translated_classes = {}
    for class_name, class_info in cd['classes'].items():
        en_name = translate_label(class_name)
        translated_classes[en_name] = {
            'attributes': [translate_label(a) for a in class_info['attributes']],
            'methods': [translate_label(m) for m in class_info['methods']],
        }

    translated_rels = []
    for rel in cd['relationships']:
        translated_rels.append({
            'from': translate_label(rel['from']),
            'to': translate_label(rel['to']),
            'type': rel['type'],
        })

    return {'classes': translated_classes, 'relationships': translated_rels}


def translate_use_case_diagram(uc: dict) -> dict:
    """Translate all labels in a use case diagram to English."""
    translated_actors = {}
    for actor_name, actor_data in uc['actors'].items():
        en_actor = translate_label(actor_name)

        translated_ucs = []
        for uc_entry in actor_data['use_cases']:
            translated_ucs.append({
                'id': uc_entry['id'],
                'type': uc_entry['type'],
                'name': translate_use_case_name(uc_entry['name']),
            })

        translated_actors[en_actor] = {
            'use_cases': translated_ucs,
            'relationships': actor_data['relationships'],  # IDs, no translation needed
        }

    return {'actors': translated_actors}


def translate_all(parsed_data: dict) -> dict:
    """Translate all parsed data from Spanish to English."""
    translated = {}
    for us_id, us_data in parsed_data.items():
        translated[us_id] = {
            'class_diagram': translate_class_diagram(us_data['class_diagram']),
            'use_case_diagram': translate_use_case_diagram(us_data['use_case_diagram']),
            'user_story_text': us_data['user_story_text'],  # Already in English
        }
    return translated


def print_translation_report(original: dict, translated: dict):
    """Print a report showing what was translated."""
    all_original_labels = set()
    all_translated_labels = set()
    untranslated = set()

    for us_id in original:
        orig_cd = original[us_id]['class_diagram']
        trans_cd = translated[us_id]['class_diagram']

        for orig_name, trans_name in zip(orig_cd['classes'].keys(), trans_cd['classes'].keys()):
            all_original_labels.add(orig_name)
            all_translated_labels.add(trans_name)
            if orig_name == trans_name and not orig_name.isascii():
                untranslated.add(orig_name)

        orig_uc = original[us_id]['use_case_diagram']
        trans_uc = translated[us_id]['use_case_diagram']

        for orig_actor, trans_actor in zip(orig_uc['actors'].keys(), trans_uc['actors'].keys()):
            all_original_labels.add(orig_actor)
            all_translated_labels.add(trans_actor)
            if orig_actor == trans_actor and not orig_actor.isascii():
                untranslated.add(orig_actor)

    print(f"Total unique original labels: {len(all_original_labels)}")
    print(f"Total unique translated labels: {len(all_translated_labels)}")
    if untranslated:
        print(f"\nUntranslated labels ({len(untranslated)}):")
        for label in sorted(untranslated):
            print(f"  - {label}")
    else:
        print("All labels translated successfully.")


