def translate_label(label: str) -> str:
    """Translate a Spanish label to English using the translation table.

    Handles compound names with underscores and camelCase.
    """
    if not label:
        return label

    # Direct lookup first
    if label in TRANSLATION_TABLE:
        return TRANSLATION_TABLE[label]

    # Case-insensitive lookup
    lower_tt = {k.lower(): v for k, v in TRANSLATION_TABLE.items()}
    if label.lower() in lower_tt:
        return lower_tt[label.lower()]

    # Try splitting on underscore and translating parts
    if '_' in label:
        parts = label.split('_')
        translated_parts = [translate_label(p) for p in parts]
        return '_'.join(translated_parts)

    # If it looks like a method name (starts with lowercase verb)
    lower = label.lower()
    for es_verb, en_verb in METHOD_TRANSLATIONS.items():
        if lower.startswith(es_verb):
            rest = label[len(es_verb):]
            if rest and rest[0] == '_':
                rest_translated = translate_label(rest[1:])
                return f"{en_verb}_{rest_translated}"
            return en_verb + rest

    # Try UC_NOUN_MAP as fallback (defined later but available at call time)
    if lower in UC_NOUN_MAP:
        en = UC_NOUN_MAP[lower]
        # Preserve original capitalization pattern
        if label[0].isupper() and en[0].islower():
            return en.capitalize()
        return en

    # Return original if no translation found (may be a proper noun or already English)
    return label


